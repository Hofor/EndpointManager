Function Write_Log {
    param(
        [string]$Message_Type, 
        [string]$Message
    )

    $MyDate = "[{0:MM/dd/yy} {0:HH:mm:ss}]" -f (Get-Date)
    Add-Content $LogFile "$MyDate - $Message_Type : $Message"
}

# Log file setup
[string]$FileName = "KioskReboot.log"
$LogFilePath = Join-Path -Path $env:programdata -ChildPath "Microsoft\IntuneManagementExtension\Logs\$FileName"
$LogFile = $LogFilePath

# Ensure log directory exists
$logDir = Split-Path -Path $LogFilePath -Parent
if (-not (Test-Path -Path $logDir)) {
    New-Item -Path $logDir -ItemType Directory -Force
}

try 
{
    Write_Log -Message_Type "INFO" -Message "Stopper Edge"
    get-process -ProcessName "msedge" | Stop-Process -Force -Confirm:$false -ErrorAction SilentlyContinue
    Start-Sleep(10)
    taskkill /IM msedge.exe /F
    Start-Sleep(10)
    Write_Log -Message_Type "INFO" -Message "Genstarter Kiosk"
    Restart-Computer -Force -Confirm:$false -ErrorAction SilentlyContinue
}
catch 
{
    Write_Log -Message_Type "INFO" -Message "Catch - Genstarter Kiosk"
}

